# declaring a string
greeting = "Hello, umar"

# accessing charectors
print(greeting[0])    #H
print(greeting[-1])   #r
print(greeting[5])    #,
print(greeting[-4])   #u

# slicing
print(greeting[0:5])
print(greeting[:7])

#methods
print(greeting.upper())    #HELLO, UMAR
print(greeting.lower())    #hello, umar
print(greeting.replace( "umar", "captain"))    #Hello, captain
