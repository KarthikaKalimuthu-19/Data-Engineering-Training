age = int(input("Enter your age: "))

if age >= 18:
    print("You can vote")
else:
    print("You are too young to vote")


marks = int(input("Enter your marks: "))

if marks >= 90:
    print("Grade A")
elif marks >= 75:
    print("Grade B")
elif marks >= 50:
    print("Grade C")
else:
    print("Grade F")